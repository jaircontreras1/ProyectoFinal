package codestartia.model;

public class Tip {
    private int idTip;
    private String titulo;
    private String consejo;
    private String tipo;
    private String prioridad;

    public int getIdTip() { return idTip; }
    public void setIdTip(int idTip) { this.idTip = idTip; }
    public String getTitulo() { return titulo; }
    public void setTitulo(String titulo) { this.titulo = titulo; }
    public String getConsejo() { return consejo; }
    public void setConsejo(String consejo) { this.consejo = consejo; }
    public String getTipo() { return tipo; }
    public void setTipo(String tipo) { this.tipo = tipo; }
    public String getPrioridad() { return prioridad; }
    public void setPrioridad(String prioridad) { this.prioridad = prioridad; }
}
